/**
 * Math module
 */
export namespace _math {
    /**
     * Add two numbers
     * @param a First number
     * @param b Second number
     */
    export function add(a: number, b: number) {
        return a + b;
    }

    /**
     * Subtract two numbers
     * @param a First number
     * @param b Second number
     */
    export function subtract(a: number, b: number) {
        return a - b;
    }
}